package jvm.reference;

/**
 * 类实现描述：
 *
 * @author 谢岳金 2018年10月30日 下午2:52
 */
public class Person {

    private String name;
    private int    age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
